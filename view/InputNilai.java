    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        if (selectedStudent != null && krs != null) {
            selectedStudent.addKRS(krs);
            
            // Memastikan data krs, course, dan lecturer tidak null saat dibaca ke dalam tabel
            String namaMatkul = (krs.getCourse() != null) ? krs.getCourse().getCourseName() : "-";
            int sks = (krs.getCourse() != null) ? krs.getCourse().getSKS() : 0;
            String namaDosen = (selectedLecturer != null) ? selectedLecturer.getName() : "-";

            Object[] rowData = {
                selectedStudent.getNIM(), 
                selectedStudent.getName(), 
                namaMatkul, 
                sks, 
                krs.getGrade(), 
                namaDosen
            };
            
            DefaultTableModel model = (DefaultTableModel) jTableNilai.getModel();
            model.addRow(rowData);
        }
    }//GEN-LAST:event_buttonAddActionPerformed

