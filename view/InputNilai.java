    private void setKRS(){
        // Validasi pengaman agar tidak melempar NullPointerException jika komponen belum siap
        if (cmbMataKuliah.getSelectedItem() == null || cmbLecturer.getSelectedItem() == null) {
            return;
        }

        this.selectedCourse = (Course) cmbMataKuliah.getSelectedItem();
        this.selectedLecturer = (Lecturer) cmbLecturer.getSelectedItem();

        try {
            int uas = Integer.parseInt(jTextFieldNilaiUAS.getText().trim());
            int uts = Integer.parseInt(jTextFieldNilaiUTS.getText().trim());
            int sikap = Integer.parseInt(jTextFieldNilaiSikap.getText().trim());
            double rataRata = (uas + uts + sikap) / 3.0;
            
            // Menggunakan constructor KRS(Course, double) sesuai dengan file KRS kamu
            this.krs = new KRS(selectedCourse, rataRata);
            
            // Tambahkan setter di kelas KRS kamu jika ini merah, atau sesuaikan logikanya
            // this.krs.setLecturer(selectedLecturer); 
            
            this.krs.setSemester(String.valueOf(jComboBoxSM.getSelectedItem()));
            
            if(this.krs != null){
                lblOutputGrade.setText(this.krs.getGrade());
            }
        } catch (NumberFormatException e) {
            lblOutputGrade.setText("Nilai Invalid");
        }
    }

