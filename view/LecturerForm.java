    // Fungsi mengambil data dari database lalu memasukannya ke JTable
    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) tableDosen.getModel();
        model.setRowCount(0); // Bersihkan isi tabel lama
        
        List<Lecturer> lecturers = controller.getLecturers();
        for (Lecturer l : lecturers) {
            Object[] row = {
                l.getLecturerID(),
                l.getName(),
                l.getNik(),
                l.getNidn(),
                l.getBidangKeahlian()
            };
            model.addRow(row);
        }
    }

    // Fungsi mendeteksi klik baris tabel agar data pindah kembali ke form input
    private void initTableListener() {
        tableDosen.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int selectedRow = tableDosen.getSelectedRow();
                if (selectedRow != -1) {
                    selectedLecturerID = Integer.parseInt(tableDosen.getValueAt(selectedRow, 0).toString());
                    txtNama.setText(tableDosen.getValueAt(selectedRow, 1).toString());
                    txtNIK.setText(tableDosen.getValueAt(selectedRow, 2).toString());
                    // Antisipasi jika data NIDN atau Keahlian bernilai null di database
                    txtNIDN.setText(tableDosen.getValueAt(selectedRow, 3) != null ? tableDosen.getValueAt(selectedRow, 3).toString() : "");
                    txtKeahlian.setText(tableDosen.getValueAt(selectedRow, 4) != null ? tableDosen.getValueAt(selectedRow, 4).toString() : "");
                    
                    btnSimpan.setText("Update"); // Ubah text tombol menjadi Update
                }
            }
        });
    }

