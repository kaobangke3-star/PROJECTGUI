    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String keyword = txtPencarian.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) tableDosen.getModel();
        model.setRowCount(0);
        
        List<Lecturer> lecturers = controller.getLecturers();
        for (Lecturer l : lecturers) {
            if (keyword.isEmpty() || keyword.equals("pencarian") || l.getName().toLowerCase().contains(keyword)) {
                Object[] row = {
                    l.getLecturerID(),
                    l.getName(),
                    l.getNik(),
                    l.getNidn(),
                    l.getBidangKeahlian()
                };
                model.addRow(row);
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

