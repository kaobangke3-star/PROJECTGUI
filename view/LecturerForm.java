    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedLecturerID == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih data dosen pada tabel terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus dosen ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int result = controller.deleteLecturer(selectedLecturerID);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data dosen berhasil dihapus!");
                loadTableData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data dosen.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String name = txtNama.getText().trim();
        String nik = txtNIK.getText().trim();
        String nidn = txtNIDN.getText().trim();
        String keahlian = txtKeahlian.getText().trim();
        
        if (name.isEmpty() || nik.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan NIK tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (selectedLecturerID == -1) {
            // MODE INSERT (Dosen Baru)
            // Memanggil Constructor: Lecturer(String nik, String name, String nidn, String bidangKeahlian)
            Lecturer newLecturer = new Lecturer(nik, name, nidn, keahlian);
            
            int result = controller.addLecturer(newLecturer);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data dosen baru berhasil disimpan!");
                loadTableData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data dosen.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // MODE UPDATE (Edit data dosen terpilih)
            // Memanggil Constructor Database: Lecturer(int lecturerID, String nik, String nidn, String name)
            Lecturer updatedLecturer = new Lecturer(selectedLecturerID, nik, nidn, name);
            updatedLecturer.setBidangKeahlian(keahlian);
            
            int result = controller.updateLecturer(updatedLecturer);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data dosen berhasil diperbarui!");
                loadTableData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui data dosen.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

