    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String keyword = txtPencarian.getText().trim().toLowerCase();
        if(keyword.isEmpty() || keyword.equals("pencarian")) {
            loadTableData();
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tableStudent.getModel();
        model.setRowCount(0);
        
        List<Student> students = studentController.getStudents();
        for (Student s : students) {
            if (s.getName().toLowerCase().contains(keyword) || s.getNim().contains(keyword)) {
                model.addRow(new Object[]{
                    s.getStudentID(), s.getName(), s.getNim(), s.getNik(), s.getStudiProgram()
                });
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText().trim();
        String nim = txtNIM.getText().trim();
        String nik = txtNIK.getText().trim();
        String prodi = cmbProdi.getSelectedItem().toString();

        // Validasi inputan kosong
        if (name.isEmpty() || nim.isEmpty() || nik.isEmpty() || cmbProdi.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Harap lengkapi semua field dan pilih program studi!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (selectedStudentID == -1) {
            // Aksi Tambah Data Baru (Insert)
            Student newStudent = new Student(nik, name, nim, prodi);
            int success = studentController.addStudent(newStudent);
            if (success > 0) {
                JOptionPane.showMessageDialog(this, "Data Mahasiswa berhasil ditambahkan!");
                loadTableData();
                resetForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan data.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Aksi Sunting Data Lama (Update)
            Student updatedStudent = new Student(nik, nim, name, prodi, selectedStudentID);
            int success = studentController.updateStudent(updatedStudent);
            if (success > 0) {
                JOptionPane.showMessageDialog(this, "Data Mahasiswa berhasil diperbarui!");
                loadTableData();
                resetForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui data.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedStudentID == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data pada tabel yang ingin dihapus terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus mahasiswa ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int success = studentController.deleteStudent(selectedStudentID);
            if (success > 0) {
                JOptionPane.showMessageDialog(this, "Data Mahasiswa berhasil dihapus!");
                loadTableData();
                resetForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

