    // Memuat seluruh data mahasiswa dari database ke komponen JTable
    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) tableStudent.getModel();
        model.setRowCount(0); // Bersihkan tabel terlebih dahulu
        
        List<Student> students = studentController.getStudents();
        for (Student s : students) {
            Object[] row = {
                s.getStudentID(),
                s.getName(),
                s.getNim(),
                s.getNik(),
                s.getStudiProgram()
            };
            model.addRow(row);
        }
    }
    
    // Listener saat baris di JTable diklik agar memuat datanya kembali ke form input
    private void initTableEvent() {
        tableStudent.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = tableStudent.getSelectedRow();
                if (row != -1) {
                    selectedStudentID = Integer.parseInt(tableStudent.getValueAt(row, 0).toString());
                    txtName.setText(tableStudent.getValueAt(row, 1).toString());
                    txtNIM.setText(tableStudent.getValueAt(row, 2).toString());
                    txtNIK.setText(tableStudent.getValueAt(row, 3).toString());
                    
                    String prodi = tableStudent.getValueAt(row, 4).toString();
                    cmbProdi.setSelectedItem(prodi);
                }
            }
        });
    }

