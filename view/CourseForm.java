    // Fungsi untuk memuat semua data dari database ke JTable
    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Bersihkan isi tabel lama
        
        List<Course> list = courseController.getCourses();
        for (Course c : list) {
            Object[] row = {
                c.getCourseID(),
                c.getCode(),
                c.getCourseName(),
                c.getSKS(),
                c.getSemester()
            };
            model.addRow(row);
        }
    }

    // Fungsi untuk mengosongkan form inputan
