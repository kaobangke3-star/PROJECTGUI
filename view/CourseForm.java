    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih matakuliah yang ingin dihapus pada tabel terlebih dahulu.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus matakuliah ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int courseID = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
            int result = courseController.deleteCourse(courseID);

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                loadTableData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal Menghapus Data!");
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String code = txtKode.getText().trim();
        String name = txtNama.getText().trim();
        String sksText = txtSKS.getText().trim();
        String semester = txtSemester.getText().trim();

        if (code.isEmpty() || name.isEmpty() || sksText.isEmpty() || semester.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom data wajib diisi!");
            return;
        }

        int sks;
        try {
            sks = Integer.parseInt(sksText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nilai SKS harus berupa angka!");
            return;
        }

        int selectedRow = jTable1.getSelectedRow();
        
        if (selectedRow == -1) {
            // INSERT BARU
            Course newCourse = new Course(code, name, 0, sks);
            int result = courseController.addCourse(newCourse);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
                loadTableData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal Menyimpan Data!");
            }
        } else {
            // UPDATE DATA LAMA
            int courseID = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
            Course updatedCourse = new Course(code, name, courseID, sks);
            int result = courseController.updateCourse(updatedCourse);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Diperbarui!");
                loadTableData();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal Memperbarui Data!");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

