    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String keyword = txtPencarian.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        List<Course> list = courseController.getCourses();
        for (Course c : list) {
            if (c.getCode().toLowerCase().contains(keyword) || 
                c.getCourseName().toLowerCase().contains(keyword) || 
                c.getSemester().toLowerCase().contains(keyword)) {
                
                Object[] row = {
                    c.getCourseID(),
                    c.getCode(),
                    c.getCourseName(),
                    c.getSKS(),
                    c.getSemester()
                };
                model.addRow(row);
            }
        }
    }//GEN-LAST:event_btnCariActionPerformed

